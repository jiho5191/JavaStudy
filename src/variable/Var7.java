// 변수 타입1
package variable;

public class Var7 {

    public static void main(String[] args) {
        int a = 100;        // 정수
        double b = 10.5;    // 실수
        boolean c = false;  // 불리언(boolean) true, false 입력 가능
        char d = 'A';       // 문자 하나
        String e = "Hello Java";   // 문자열, 문자열을 다루기 위한 특별한 타입
        // 변수의 값은 변할 수 있지만 리터널은 개발자가 직접 입력한 고정된 값이다. 리터럴 자체는 변하지 x. ex) 100, 10.5, false, 'A'

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
